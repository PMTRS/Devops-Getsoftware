package Switchcase;

public class Switchclass2 {

	public static void main(String[] args) {
		
				
		String d = "TH";
		switch(d)
		{
		case "MD" : System.out.println("Monday");
		          break;
		case "FR" : System.out.println("Friday");
		 		break;
		case "TUE" : System.out.println("Tuesday");
				break;
		case "SA" : System.out.println("Saturday");
		 		break;
		case "WD" : System.out.println("Wednesday");
		         break;
		case "TH"   : System.out.println("Thursday");  
		         break;
		case "SU"    : System.out.println("Sunday");
		         break;
		default     : System.out.println("Enter CODE is INCORRECT");
		         break;
		}
		
		

	}

}
